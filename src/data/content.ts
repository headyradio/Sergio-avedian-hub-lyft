// ─── Helpers ───
export function getContentBySlug(slug: string): ContentItem | undefined {
  return contentItems.find((c) => c.slug === slug)
}

export function getContentByTopic(topicSlug: string): ContentItem[] {
  return contentItems.filter((c) => c.topicSlugs.includes(topicSlug))
}

export function getFeaturedContent(): ContentItem[] {
  return contentItems.filter((c) => c.isFeatured)
}

export function getBeginnerContent(): ContentItem[] {
  return contentItems.filter((c) => c.difficulty === "beginner")
}

export function getStartHereContent(): ContentItem[] {
  return contentItems.filter((c) => c.isStartHere)
}

export function searchContent(query: string): ContentItem[] {
  const q = query.toLowerCase()
  return contentItems.filter(
    (c) =>
      c.title.toLowerCase().includes(q) ||
      c.description.toLowerCase().includes(q) ||
      c.tags.some((t) => t.toLowerCase().includes(q))
  )
}
