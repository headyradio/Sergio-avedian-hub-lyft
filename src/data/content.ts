// Article body structure — simple block-based for MVP rendering.
export type ArticleBlock =
  | { kind: "p"; text: string }
  | { kind: "h2"; text: string }
  | { kind: "h3"; text: string }
  | { kind: "ul"; items: string[] }
  | { kind: "ol"; items: string[] }
  | { kind: "callout"; variant: "tip" | "warning" | "insight"; title?: string; text: string }
  | { kind: "quote"; text: string; attribution?: string }

export type ArticleBody = {
  /** Short editor-written deck that sits under the title. */
  dek?: string
  /** Ordered list of blocks rendered top to bottom. */
  blocks: ArticleBlock[]
  /** Bulleted key takeaways rendered at the bottom. */
  takeaways?: string[]
}

